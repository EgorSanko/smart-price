    def _define_tools(self) -> list[dict]:
        return [
            {
                "name": "search_products",
                "description": (
                    "Search for products across marketplaces (Onliner, Yandex Market). "
                    "Use when user asks to find, recommend, compare or pick products. "
                    "Returns real prices and product names from stores. "
                    "IMPORTANT: Use EXACT model names like 'Samsung Galaxy S25 Ultra'. "
                    "For comparisons, call this tool TWICE - once for each product!"
                ),
                "input_schema": {
                    "type": "object",
                    "properties": {
                        "query": {
                            "type": "string",
                            "description": "Search query - use FULL model name!",
                        },
                        "max_price": {
                            "type": "number",
                            "description": (
                                "Maximum price filter. In RUB for Russia, BYN for Belarus."
                            ),
                        },
                        "region": {
                            "type": "string",
                            "enum": ["RU", "BY", "all"],
                            "description": "RU = Russia, BY = Belarus, all = both",
                        },
                    },
                    "required": ["query"],
                },
            }
        ]

    async def _execute_tool(self, name: str, input_data: dict) -> str:
        if name != "search_products" or not self.search_fn:
            return "Tool not available"

        query = input_data.get("query", "")
        region = input_data.get("region", "all")
        max_price = input_data.get("max_price")

        found = await self.search_fn(query, region, max_price)

        if not found:
            return f"По запросу '{query}' ничего не найдено."

        result = f"Найдено {len(found)} предложений:\n"
        for i, p in enumerate(found[:12], 1):
            price_str = p.get("price", "")
            shop = p.get("shop", "")
            result += f"{i}. {p.get('title', '?')} — {price_str} ({shop})"
            specs = p.get("specs", "")
            if specs:
                result += f" | {specs[:80]}"
            result += "\n"

        return result

    async def _execute_tool_with_products(
        self, name: str, input_data: dict
    ) -> tuple[str, list[dict]]:
        if name != "search_products" or not self.search_fn:
            return "Tool not available", []

        query = input_data.get("query", "")
        region = input_data.get("region", "all")
        max_price = input_data.get("max_price")

        logger.info("search_products", query=query, region=region, max_price=max_price)

        found = await self.search_fn(query, region, max_price)

        # Deduplicate
        seen_titles: set[str] = set()
        unique: list[dict] = []
        for p in found:
            short = p.get("title", "")[:50].lower()
            if short not in seen_titles:
                seen_titles.add(short)
                unique.append(p)
            if len(unique) >= 12:
                break

        # Build text for Claude (include category so AI can judge relevance)
        if found:
            result = f"Найдено {len(found)} предложений:\n"
            for i, p in enumerate(unique[:12], 1):
                cat = p.get("category", "")
                cat_str = f" [Категория: {cat}]" if cat else ""
                result += (
                    f"{i}. {p.get('title', '?')} — "
                    f"{p.get('price', '?')} ({p.get('shop', '?')}){cat_str}"
                )
                if p.get("specs"):
                    result += f" | {p['specs'][:80]}"
                result += "\n"
            result += "\nВАЖНО: Проверь что результаты соответствуют запросу пользователя! Если категория или товар не тот — НЕ показывай его."
        else:
            result = f"По запросу '{query}' ничего не найдено."

        return result, unique[:10]
