def is_accessory(title: str) -> bool:
    """Check if a product title indicates an accessory."""
    t = title.lower()
    return any(kw in t for kw in ACCESSORY_KEYWORDS)


# Type alias for the search function
SearchFn = Callable[..., Coroutine[Any, Any, list[dict]]]


class ShoppingAgent(BaseAgent):
    """AI shopping consultant — ЕГОРУШКА."""

    def __init__(self, search_fn: SearchFn | None = None) -> None:
        self.search_fn = search_fn  # async function(query, region, max_price) -> list[dict]
